// @lc code=start
func lengthOfLongestSubstring(s string) int {
	if s == "" {
		return 0
	}

	records := map[byte]int{
		s[0]: 0,
	}
	ans := 1
	for i, j := 0, 1; i < j && j < len(s); j++ {
		if k, ok := records[s[j]]; ok {
			for m := i; m <= k; m++ {
				delete(records, s[m])
			}
			i = k + 1
		} else {
			ans = max(ans, j-i+1)
		}

		records[s[j]] = j
	}

	return ans
}

func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}

